package dataset.painter.style.enums;

/**
 * Arrows styles.
 *
 * @author MTomczyk
 */
public enum Arrow
{
    /**
     * Not used.
     */
    NONE,

    /**
     * Triangular (for 2D visualization).
     */
    TRIANGULAR_2D,

    /**
     * Triangular (the arrow marker is reversed; for 2D visualization).
     */
    TRIANGULAR_REVERSED_2D,

    /**
     * Triangular (for 2D visualization).
     */
    TRIANGULAR_3D,

    /**
     * Triangular (the arrow marker is reversed; for 2D visualization).
     */
    TRIANGULAR_REVERSED_3D,
}
