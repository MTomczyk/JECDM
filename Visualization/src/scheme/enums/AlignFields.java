package scheme.enums;

/**
 * Plot components that can be aligned.
 */
public enum AlignFields
{
    /**
     * Title component.
     */
    TITLE,

    /**
     * Legend component/
     */
    LEGEND,

    /**
     * Colorbar component.
     */
    COLORBAR,

    /**
     * X-axis component.
     */
    AXIS_X,

    /**
     * Y-axis component.
     */
    AXIS_Y,

    /**
     * Z-axis component.
     */
    AXIS_Z,

    /**
     * A1-axis (auxiliary) component.
     */
    AXIS_A1,

    /**
     * Axis (colorbar) component.
     */
    AXIS_COLORBAR,

    /**
     * X-axis (3D) component.
     */
    AXIS3D_X,

    /**
     * Y-axis (3D) component.
     */
    AXIS3D_Y,

    /**
     * Z-axis (3D) component.
     */
    AXIS3D_Z
}
