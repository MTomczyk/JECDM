package scheme.enums;

/**
 * PLot components that can be assigned a font.
 */
public enum FontFields
{
    /**
     * Title font.
     */
    TITLE,

    /**
     * Loading panel font.
     */
    LOADING_PANEL,

    /**
     * Popup menu item font.
     */
    POPUP_MENU_ITEM,

    /**
     * Legend entry font.
     */
    LEGEND_ENTRY,

    /**
     * X-axis tick label font.
     */
    AXIS_X_TICK_LABEL,

    /**
     * Y-axis tick label font.
     */
    AXIS_Y_TICK_LABEL,

    /**
     * Z-axis tick label font.
     */
    AXIS_Z_TICK_LABEL,

    /**
     * A1-axis (auxiliary) tick label font.
     */
    AXIS_A1_TICK_LABEL,

    /**
     * Axis (colorbar) tick label font.
     */
    AXIS_COLORBAR_TICK_LABEL,

    /**
     * X-axis title font.
     */
    AXIS_X_TITLE,

    /**
     * Y-axis title font.
     */
    AXIS_Y_TITLE,

    /**
     * Z-axis title font.
     */
    AXIS_Z_TITLE,

    /**
     * A1-axis (auxiliary) title font.
     */
    AXIS_A1_TITLE,

    /**
     * Axis (colorbar) title font.
     */
    AXIS_COLORBAR_TITLE,

    /**
     * X-axis (3D) tick label font.
     */
    AXIS3D_X_TICK_LABEL,

    /**
     * Y-axis (3D) tick label font.
     */
    AXIS3D_Y_TICK_LABEL,

    /**
     * Z-axis (3D) tick label font.
     */
    AXIS3D_Z_TICK_LABEL,

    /**
     * X-axis (3D) title font.
     */
    AXIS3D_X_TITLE,

    /**
     * Y-axis (3D) title font.
     */
    AXIS3D_Y_TITLE,

    /**
     * Z-axis (3D) title font.
     */
    AXIS3D_Z_TITLE,
}
