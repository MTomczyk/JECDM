package scheme.enums;

/**
 * PLot components that can be adjusted using some number.
 */
public enum NumberFields
{
    /**
     * Grid (main): lines dash pattern.
     */
    GRID_MAIN_DASH_PATTERN,

    /**
     * Grid (auxiliary): lines dash pattern.
     */
    GRID_AUX_DASH_PATTERN,

    /**
     * Legend: limits the number of entries per one column
     */
    LEGEND_NO_ENTRIES_PER_COLUMN_LIMIT
}
