package thread.swingworker;

/**
 * Simple enum representing swing worker even types
 *
 * @author MTomczyk
 */
public enum EventTypes
{
    /**
     * On plot resize event.
     */
    ON_RESIZE,

    /**
     * On data set changed event.
     */
    ON_DATA_CHANGED,

    /**
     * On data set changed event.
     */
    ON_HEATMAP_DATA_CHANGED,

    /**
     * On data set changed event.
     */
    ON_HEATMAP_FILTER_CHANGED,

    /**
     * On demand.
     */
    ON_DEMAND,

    /**
     * On interaction event.
     */
    ON_INTERACTION,

}
